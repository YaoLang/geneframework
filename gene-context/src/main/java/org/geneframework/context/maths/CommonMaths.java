public class CommonMaths {

    public static double addExact(double a,double b){
        return a+b;
    }

    public static double mulExact(double a,double b){
        return a*b;
    }

    public static double subExact(double a,double b){
        return a-b;
    }

    public static double divExact(double a,double b){
        return a/b;
    }


}
